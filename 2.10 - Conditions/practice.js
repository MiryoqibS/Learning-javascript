/*
== Задание 7 от ChatGPT ==
🌡 Температурный советчик
Пусть переменная `temperature` содержит число:
- Если температура ниже `0` — вывести: `Очень холодно`.
- От `0` до `15` — `Прохладно`.
- От `16` до `25` — `Тепло`.
- От `26` и выше — `Жарко`.
*/

let temperature = prompt("Какая температура сегодня: ");

if (+temperature < 0) {
    alert("Сегодня очень холодно")
} else if (+temperature <= 15) {
    alert("Сегодня прохладно");
} else if (+temperature <= 25) {
    alert("Сегодня Тепло");
} else {
    alert("Сегодня тепло")
}

/*
== Задание 8 от ChatGPT ==
🛂 Валидация возраста
Попроси у пользователя ввести возраст через `prompt`.
- Если возраст меньше 0 или больше 120 — `Некорректный возраст`.
- Если от 18 до 60 — `Доступ разрешён`.
- Иначе — `Возраст не подходит`.
*/

let age = prompt("Введите возраст: ");

if (age < 0 || age > 120) {
    alert("Некорветный возраст");
} else if (age >= 18 && age <= 60) {
    alert("Доступ разрешён");
} else {
    alert("Возраст не подходить");
};